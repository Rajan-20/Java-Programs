package com.ob.homework;

public class Operator {
	public static void main(String[] args) {
	//Left Shift Operator
	int a= 6;
	int b= a<<2;
	System.out.println(b);//24
	
	//Right Shift Operator
	int c = a>>2;
	System.out.println(c);//1
	
	//Negation Operator
	int d = 3;
	System.out.println(~d);
	
	}
}
